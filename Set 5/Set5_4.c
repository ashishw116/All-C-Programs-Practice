//WAP to Illustrate the use of Relational operators.
#include<stdio.h>
void main()
{
    int a,b;
    printf("Enter Number=");
    scanf("%d%d",&a,&b);
    printf("\nA=%d B=%d \nA>B=%d",a,b,(a>b));
    printf("\nA<B=%d",(a<b));
    printf("\nA>=B=%d",(a>=b));
    printf("\nA<=B=%d",(a<=b));
    printf("\nA==B=%d",(a==b));
    printf("\nA!=B=%d",(a!=b));
}
